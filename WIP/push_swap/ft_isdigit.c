#include "push_swap.h"

int	ft_isdigit(char *c)
{
	if (*c == '-')
		c++;
	while (*c != '\0')
	{
		if ('0' <= *c && *c <= '9')
			c++;
		else
			return (0);
	}
	return (1);
}
